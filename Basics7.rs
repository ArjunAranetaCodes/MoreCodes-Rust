//If, ElseIf, and Else Structure

fn main(){
 let num1 = 1;
 let num2 = 2;

 if num1 > num2 {
  println!("First number is higher!");
 }

 if num1 == num2 {
  println!("They are equal!");
 }else{
  println!("They are not equal!");
 }

 if num1 > num2 {
  println!("First number is greater!");
 }else if num1 < num2 {
  println!("Second number is greater!");
 }
}



